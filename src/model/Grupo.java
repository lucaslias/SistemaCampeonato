package model;

public class Grupo {
	
	private String grupo;
	private String codigoTime;
	
	public String getGrupo() {
		return grupo;
	}
	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}
	public String getCodigoTime() {
		return codigoTime;
	}
	public void setCodigoTime(String codigoTime) {
		this.codigoTime = codigoTime;
	}

	
}
